// Autocomplete for inventory

var listItemCounter = 0;

function autocomplete(inp, arr) {
  /*the autocomplete function takes two arguments,
  the text field element and an array of possible autocompleted values:*/
  var currentFocus;
  /*execute a function when someone writes in the text field:*/
  inp.addEventListener("input", function(e) {

    

    if (document.getElementById("inventory_search").value == ""){
      document.getElementById("inventory_search").style.borderBottomLeftRadius = "10px";
      document.getElementById("inventory_search").style.borderBottomRightRadius = "10px";
      for (var i=0; i<cardIDQuickSearch.length; i++){
        document.getElementById(cardIDQuickSearch[i]).style.display = "block";
      }
    }
    else {
      document.getElementById("inventory_search").style.borderBottomLeftRadius = "0px";
      document.getElementById("inventory_search").style.borderBottomRightRadius = "0px";

      // console.log(document.getElementById("inventory_search").value.length)


      for (i=0; i<cardQuickSearch.length; i++){
        
        // console.log(cardQuickSearch[i][document.getElementById("inventory_search").value.length-1].toUpperCase())

        // if(cardQuickSearch[i][document.getElementById("inventory_search").value.length-1].toUpperCase() != document.getElementById("inventory_search").value[document.getElementById("inventory_search").value.length-1].toUpperCase() ) {
          
        //   console.log(cardQuickSearch[i][document.getElementById("inventory_search").value.length-1].toUpperCase())
        //   document.getElementById(cardQuickSearch[i]).style.display = "none";
        // }

        if(cardQuickSearch[i][document.getElementById("inventory_search").value.length-1].toUpperCase() != document.getElementById("inventory_search").value[document.getElementById("inventory_search").value.length-1].toUpperCase() ) {
          
          
          // console.log(document.getElementsByClassName("Charizard"))
          document.getElementById(cardIDQuickSearch[i]).style.display = "none";
        }
 
        if(cardQuickSearch[i][document.getElementById("inventory_search").value.length-1].toUpperCase() == document.getElementById("inventory_search").value[document.getElementById("inventory_search").value.length-1].toUpperCase() ) {

         

          for (var j=document.getElementById("inventory_search").value.length; j>0; j--){
            if(cardQuickSearch[i][j-1].toUpperCase() == document.getElementById("inventory_search").value[j-1].toUpperCase()){
              document.getElementById(cardIDQuickSearch[i]).style.display = "block";
            }
            else {
              document.getElementById(cardIDQuickSearch[i]).style.display = "none";
            }
          }
          // console.log(counter)
          // if (counter == document.getElementById("inventory_search").value.length){
          //     document.getElementById(cardIDQuickSearch[i]).style.display = "block";
             
          // }

          
      
          // document.getElementById(cardIDQuickSearch[i]).style.display = "block";
        
        
        }
      }
        
        
        // if(cardQuickSearch[i][j])
    
      // if(cardQuickSearch[document.getElementById("inventory_search").value.length-1][document.getElementById("inventory_search").value.length-1].toUpperCase() !=document.getElementById("inventory_search").value[document.getElementById("inventory_search").value.length-1].toUpperCase() ) {
      //   console.log(cardQuickSearch[document.getElementById("inventory_search").value.length-1])
      //   document.getElementById(cardQuickSearch[0]).style.display = "none";
      // }

      // for (i=0; i <document.getElementById("inventory_search").value.length; i++) {
      //   j = 0 // 
      //   if(cardQuickSearch[i][j] != document.getElementById("inventory_search").value)

      // }


     }




      var a, b, i, val = this.value;
      /*close any already open lists of autocompleted values*/
      closeAllLists();
      if (!val) { return false;}
      currentFocus = -1;
      /*create a DIV element that will contain the items (values):*/
      a = document.createElement("DIV");
      a.setAttribute("id", this.id + "autocomplete2-list");
      a.setAttribute("class", "autocomplete2-items");
      /*append the DIV element as a child of the autocomplete container:*/
      this.parentNode.appendChild(a);
      /*for each item in the array...*/

      var counter = 0 ;
      var limit = 15;
          
        for (i = 0; i < arr.length; i++) {
          /*check if the item starts with the same letters as the text field value:*/
      
          
          if (arr[i].substr(0, val.length).toUpperCase() == val.toUpperCase()) {

            if (counter < limit){

            /*create a DIV element for each matching element:*/
            b = document.createElement("DIV");
            /*make the matching letters bold:*/
            b.innerHTML = "<strong>" + arr[i].substr(0, val.length) + "</strong>";
            b.innerHTML += arr[i].substr(val.length);
            /*insert a input field that will hold the current array item's value:*/
            b.innerHTML += "<input type='hidden' value='" + arr[i] + "'>";

            listItemCounter = listItemCounter + 1
            
            /*execute a function when someone clicks on the item value (DIV element):*/
            b.addEventListener("click", function(e) {
                /*insert the value for the autocomplete text field:*/
                inp.value = this.getElementsByTagName("input")[0].value;
                /*close the list of autocompleted values,
                (or any other open lists of autocompleted values:*/
                closeAllLists();
                // document.getElementById("myForm").submit();
            });
            a.appendChild(b);
            counter = counter + 1;
          }
           }
           
   
        }
        if (listItemCounter == 0){
          document.getElementById("inventory_search").style.borderBottomLeftRadius = "10px";
          document.getElementById("inventory_search").style.borderBottomRightRadius = "10px";
        }
        listItemCounter = 0;
      // }
   
  });
  /*execute a function presses a key on the keyboard:*/
  inp.addEventListener("keydown", function(e) {
      var x = document.getElementById(this.id + "autocomplete2-list");
      if (x) x = x.getElementsByTagName("div");
      if (e.keyCode == 40) {
        /*If the arrow DOWN key is pressed,
        increase the currentFocus variable:*/
        currentFocus++;
        /*and and make the current item more visible:*/
        addActive(x);
      } else if (e.keyCode == 38) { //up
        /*If the arrow UP key is pressed,
        decrease the currentFocus variable:*/
        currentFocus--;
        /*and and make the current item more visible:*/
        addActive(x);
      } else if (e.keyCode == 13) {
        e.preventDefault();
        if (currentFocus > -1) {
          /*and simulate a click on the "active" item:*/
          if (x) x[currentFocus].click();
        }
      }
  });
  function addActive(x) {
    /*a function to classify an item as "active":*/
    if (!x) return false;
    /*start by removing the "active" class on all items:*/
    removeActive(x);
    if (currentFocus >= x.length) currentFocus = 0;
    if (currentFocus < 0) currentFocus = (x.length - 1);
    /*add class "autocomplete-active":*/
    x[currentFocus].classList.add("autocomplete2-active");
  }
  function removeActive(x) {
    /*a function to remove the "active" class from all autocomplete items:*/
    for (var i = 0; i < x.length; i++) {
      x[i].classList.remove("autocomplete2-active");
    }
  }
  function closeAllLists(elmnt) {
    /*close all autocomplete lists in the document,
    except the one passed as an argument:*/
    var x = document.getElementsByClassName("autocomplete2-items");
    for (var i = 0; i < x.length; i++) {
      if (elmnt != x[i] && elmnt != inp) {
        x[i].parentNode.removeChild(x[i]);
      }
    }

  }
  /*execute a function when someone clicks in the document:*/
  document.addEventListener("click", function (e) {
      closeAllLists(e.target);
    document.getElementById("inventory_search").style.borderBottomLeftRadius = "10px";
    document.getElementById("inventory_search").style.borderBottomRightRadius = "10px";

  });
}

