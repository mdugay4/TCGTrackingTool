function toggleMenu() {

    if (document.getElementById("mySidenav").style.width == "250px") {
        document.getElementById("mySidenav").style.width = "0px";
        document.getElementById("more_options").style.display = "none";
        document.getElementById("more_options_trigger").style.display = "block";
        for (var i=0; i<cardIDQuickSearch.length; i++){
          document.getElementById(cardIDQuickSearch[i]).style.display = "block";
        }
        document.getElementById("inventory_search").value = ""

        return 0;
    }
    document.getElementById("mySidenav").style.width = "250px";

}

function closeMenu() {
    document.getElementById("mySidenav").style.width = "0";
    document.getElementById("more_options").style.display = "none";
    document.getElementById("more_options_trigger").style.display = "block";
    for (var i=0; i<cardIDQuickSearch.length; i++){
      document.getElementById(cardIDQuickSearch[i]).style.display = "block";
    }
    document.getElementById("inventory_search").value = ""
}


